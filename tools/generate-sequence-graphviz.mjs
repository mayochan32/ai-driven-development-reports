import { writeFile } from "node:fs/promises";

const output = new URL("../reports/003-layout-aware-diagram-as-code/assets/sample-order-sequence-graphviz.dot", import.meta.url);
const participants = [
  { id: "User", label: "ユーザー", fill: "#E8F3FF" },
  { id: "Web", label: "Web画面", fill: "#E8F3FF" },
  { id: "Order", label: "注文サービス", fill: "#DDEBE4" },
  { id: "Inventory", label: "在庫サービス", fill: "#F7FAF8" },
  { id: "Fraud", label: "不正検知", fill: "#F7FAF8" },
  { id: "Payment", label: "決済サービス", fill: "#F7FAF8" },
  { id: "Broker", label: "メッセージ基盤", fill: "#F3EFFF" },
];
const messages = [
  { from: "User", to: "Web", label: "注文を確定" },
  { from: "Web", to: "Order", label: "注文作成を依頼" },
  { phase: "par 在庫・不正審査", from: "Order", to: "Inventory", label: "在庫を引き当て" },
  { from: "Inventory", to: "Order", label: "引当結果", dashed: true },
  { from: "Order", to: "Fraud", label: "審査を依頼" },
  { from: "Fraud", to: "Order", label: "審査結果", dashed: true },
  { phase: "alt 前提条件OK", from: "Order", to: "Payment", label: "決済を依頼" },
  { phase: "alt 決済成功", from: "Payment", to: "Order", label: "決済ID", dashed: true },
  { from: "Order", to: "Order", label: "確定注文を保存" },
  { phase: "par 注文確定後", from: "Order", to: "Broker", label: "配送依頼を発行", dashed: true, async: true },
  { from: "Order", to: "Broker", label: "確認通知を発行", dashed: true, async: true },
  { from: "Order", to: "Web", label: "注文番号", dashed: true },
  { from: "Web", to: "User", label: "注文完了", dashed: true },
  { phase: "alt タイムアウト", from: "Payment", to: "Order", label: "応答なし", dashed: true, failure: true },
  { phase: "loop 最大3回", from: "Order", to: "Payment", label: "決済状態を照会" },
  { from: "Payment", to: "Order", label: "処理中または確定結果", dashed: true },
  { phase: "alt 失敗・不明", from: "Order", to: "Inventory", label: "引当を解放", failure: true },
  { from: "Order", to: "Web", label: "注文失敗", dashed: true, failure: true },
  { from: "Web", to: "User", label: "再試行を案内", dashed: true, failure: true },
  { phase: "alt 前提条件NG", from: "Order", to: "Inventory", label: "引当済みなら解放", failure: true },
  { from: "Order", to: "Web", label: "注文拒否", dashed: true, failure: true },
  { from: "Web", to: "User", label: "理由を表示", dashed: true, failure: true },
];

const q = (value) => JSON.stringify(value);
const lines = [
  "// Generated by tools/generate-sequence-graphviz.mjs",
  "digraph OrderSequence {",
  "  graph [rankdir=TB, bgcolor=\"white\", pad=0.25, nodesep=0.38, ranksep=\"0.52 equally\", splines=line, fontname=\"Helvetica\", label=\"EC注文処理のシーケンス図\", labelloc=t];",
  "  node [fontname=\"Helvetica\"];",
  "  edge [fontname=\"Helvetica\", fontsize=9, arrowsize=0.7, color=\"#52645D\"];",
  "",
];

for (const participant of participants) {
  lines.push(`  ${participant.id}_head [shape=box, style=\"rounded,filled\", fillcolor=\"${participant.fill}\", color=\"#45635A\", label=${q(participant.label)}, group=${q(participant.id)}];`);
}
lines.push("  phase_head [shape=plaintext, label=\"\", group=\"phase\"];");
lines.push(`  { rank=same; phase_head; ${participants.map(({ id }) => `${id}_head`).join("; ")}; }`);
lines.push(`  phase_head -> ${participants.map(({ id }) => `${id}_head`).join(" -> ")} [style=invis, weight=100];`);
lines.push("");

for (let index = 0; index < messages.length; index += 1) {
  const row = String(index + 1).padStart(2, "0");
  const message = messages[index];
  lines.push(`  phase_${row} [shape=plaintext, fontsize=9, fontcolor=\"#52645D\", label=${q(message.phase ?? "")}, group=\"phase\"];`);
  for (const participant of participants) {
    lines.push(`  ${participant.id}_${row} [shape=point, width=0.01, height=0.01, label=\"\", style=invis, group=${q(participant.id)}];`);
  }
  lines.push(`  { rank=same; phase_${row}; ${participants.map(({ id }) => `${id}_${row}`).join("; ")}; }`);
  lines.push(`  phase_${row} -> ${participants.map(({ id }) => `${id}_${row}`).join(" -> ")} [style=invis, weight=100];`);
  lines.push("");
}

for (const participant of participants) {
  const timeline = [`${participant.id}_head`, ...messages.map((_, index) => `${participant.id}_${String(index + 1).padStart(2, "0")}`)];
  lines.push(`  ${timeline.join(" -> ")} [arrowhead=none, style=dashed, color=\"#9CB7AC\", weight=20];`);
}
lines.push("");

messages.forEach((message, index) => {
  const row = String(index + 1).padStart(2, "0");
  const attributes = [
    "constraint=false",
    `label=${q(message.label)}`,
    message.dashed ? "style=dashed" : null,
    message.failure ? "color=\"#B42318\"" : null,
    message.async ? "color=\"#6D4AFF\"" : null,
  ].filter(Boolean);
  lines.push(`  ${message.from}_${row} -> ${message.to}_${row} [${attributes.join(", ")}];`);
});

lines.push("}", "");
await writeFile(output, lines.join("\n"), "utf8");
